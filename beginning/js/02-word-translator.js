let language = prompt('("es", "de", "en", and "fr"');
switch (language) {
case "es":
document.write("Hola Mundo");
break;
case "de":
document.write("Hallo Wereld");
break;
case "en":
document.write("Hello World");
break;
case "fr":
document.write("Bonjour le monde");
break;
default:
document.write("Hello World!");
}